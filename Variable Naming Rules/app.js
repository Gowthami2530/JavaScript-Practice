// Variable Naming Rules 
// can contain digits , letters, underscore,$
// Must start with letter,$ or _
// no keyword should be taken as variable name
// cannot start with number as the variable name
// case sensitive - fullname vs FullName
// camelCase or underscore

let random_1234_$ = "Gowthami";
console.log(random_1234_$);

let _$random_1234_$ = "Gowthami";
console.log(_$random_1234_$);

